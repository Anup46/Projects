    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        
        String name = this.name.getText();
        String email = this.email.getText();
        String pass = new String(password.getPassword());
        
        if(this.name.getText().equals("") || this.email.getText().equals("") || password.getText().equals(""))
        {
        JOptionPane.showMessageDialog(null, "One of the above textfield are empty!! ");
        }
        else
        {
        User u = new User();
        u.setID(0);
        u.setName(name);
        u.setEmail(email);
        u.setPassword(pass);
        
        UserController uc = new UserController();
        
        int res = uc.createAccount(u);
        
        if (res > 0) {
            JOptionPane.showMessageDialog(null, "Your information is successfully saved!! ");
            new login().setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Unable to save");
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    }
